# Data ini juga salah satu tipe koleksi data
# Data yang berpasangan, dengan sebutan key : value

# Contoh
data = {
    "nama": "Bayu",
    "umur": 27,
    "pekerjaan": "Programmer"
}

print(data)

# Akses elemen dalam dictionary
print(data["nama"])
print(data["umur"])
print(data["pekerjaan"])

# Ubah data dictionary
data["pekerjaan"] = "Digital Marketing"
print(data)